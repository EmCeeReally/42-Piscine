int	ft_str_is_lowercase(char *str)
{
	int	i;

	i = 0;
	while (i[str])
	{
		if (!(i[str] >= 'a' && i[str] <= 'z'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
