char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (i[str])
	{
		if (i[str] >= 'a' && i[str] <= 'z')
		{
			i[str] -= 32;
		}
		i++;
	}
	return (str);
}
